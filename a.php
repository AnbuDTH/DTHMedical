				<a class="cd-faq-trigger" >Certification process</a>
				<div class="cd-faq-content" style="display: block;" >
				<p>	Certification process:
NPTEL began the initiative of offering certification to students for courses in March 2014. The process of certification is as follows.</p>

<p>1. Subject Matter Experts (SME - faculty from IITs or partner institutes with input from industry) create recorded video content for courses.</p>
<p>2. The course is uploaded on the portal and opened for enrollments, which is free.</p>
<p>3. Every week, about 3 hrs of video content is released along with an assignment , which is evaluated and provides the student with a score.</p>
<p>4. Teaching Assistants (TAs) and the faculty members support the discussion forum – answering questions and clearing doubts.</p>
<p>5. If someone wants to get a certificate from the IITs/IISc after doing the course, he/she should register for the in-person proctored certification exam that is conducted in 100+ cities across India in collaboration with an exam partner. The certification exam is not free but has a nominal fee of Rs 1000.</p>
<p>6. Final score=25% assignment score + 75% exam score.  
<b>The pass criteria for exams is being changed from July 2019. A learner will pass and be certified only if
Average assignment score (out of 100) >= 40 AND Final exam score ( out of 100) >= 40.</b>
E-verifiable certificates are made available on nptel.ac.in/noc 
Only e-certificates will be published. Hard copies of certificates will NOT be printed.</p>
<p>7. These certificates are envisioned for use in credit transfer to universities or for making the student more employable or for enhancing his growth in his current place of work.</p>

