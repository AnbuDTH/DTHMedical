				<a class="cd-faq-trigger" >Why was this initiative through the model of Open Online Courses?</a>
				<div class="cd-faq-content" style="display: block;" >
					<p>Massive Open Online Courses (MOOC) is essentially an asynchronous platform and a process for teaching through pre-recorded lectures, resource video materials, lecture notes, assignments and quizzes, which are usually online and provide self assessment in regular intervals during learning. </p>
<p>The learning, through scheduling of fixed time duration for completion of courses and, therefore, the simultaneous participation of teachers and a large number of students may be termed synchronous and is thus similar to a classroom, albeit on the Internet and being much larger in size. </p>
           <p>When offered with consideration for students in non-urban and rural areas through supplementary DVDs and mobile delivered content, they enable quality and equitable access to a much larger population of students and can lead to a significant rise in the Gross Enrollment Ratio.</p>
				<p>These courses are open for anyone to access – at no cost. So anyone who is interested in learning gets access to quality content, which also includes discussion with the content creator and access to assignments for self testing.</p>
				<p>The faculty who are currently offering courses are from the IITs or from other reputed institutes such as CMI, IMSc etc.</p>
