				<a class="cd-faq-trigger" >The National Programme on Technology Enhanced Learning (popularly known as NPTEL)</a>
				<div class="cd-faq-content" style="display: block;text-align:justify">
					<p>NPTEL is the largest and most appreciated exercise in online learning in technical
education in the world with more than 2300 courses in engineering, technology, arts and
humanities published through the official website <a href="http://nptel.ac.in">http://nptel.ac.in.</a> Seven IITs (IIT Bombay,
IIT Delhi, IIT Guwahati, IIT Kanpur, IIT Kharagpur, IIT Madras and IIT Roorkee) and the Indian
Institute of Science,Bangalore are official partners and share the responsibility for creating
electronic content in the form of video lectures or web based lecture materials or both.
More than 2000 faculty members from across the country have created lectures since 2003
and have also made them available through YouTube also, which has led to many technical
institutions adopting them. The main objective was to fulfil the need for supplementary
lectures provided by some of the experienced teachers from renowned academic
institutions. The lectures are usually in the form of modules and adequate for a one
semester course of 40 hours on each topic; recently, NPTEL has also adopted the Massive
Open Online Course (MOOC) model with proctored examinations. YouTube alone has
recorded more than 1 Billion visits since the project was launched in November 2007 and
is the most visited education channel in the world.</p>
