						<div class="well fawell" data-toggle="collapse" data-target="#1" data-parent="#accordion">1. What is NPTEL?<i class="glyphicon glyphicon-menu-up pull-right"></i></div>
							<ul id="1" class="panel-collapse collapse in fadata">
								NPTEL is an acronym for National Programme on Technology Enhanced Learning which is an initiative by seven Indian Institutes of Technology (IIT Bombay, Delhi, Guwahati, Kanpur, Kharagpur, Madras and Roorkee) and Indian Institute of Science (IISc) for creating course contents in engineering and science.
								NPTEL as a project originated from many deliberations between IITs, Indian Institutes of Management (IIMs) and Carnegie Mellon University (CMU) during the years 1999-2003. A proposal was jointly put forward by five IITs (Bombay, Delhi, Kanpur, Kharagpur and Madras) and IISc for creating contents for 100 courses as web based supplements and 100 complete video courses, for forty hours of duration per course. Web supplements were expected to cover materials that could be delivered in approximately forty hours. Five engineering branches (Civil, Computer Science, Electrical, Electronics and Communication and Mechanical) and core science programmes that all engineering students are required to take in their undergraduate engineering programme in India were chosen initially. Contents for the above courses were based on the model curriculum suggested by All India Council for Technical Education (AICTE) and the syllabi of major affiliating Universities in India.
							</ul>
					</div>	
					<div class="panel faqpanel">
						<div class="well fawell" data-toggle="collapse" data-target="#2" data-parent="#accordion">2. Why NPTEL?<i class="glyphicon glyphicon-menu-down pull-right"></i></div>
						<ul id="2" class="panel-collapse collapse fadata">
							<li>The basic objective of science and engineering education in India is to devise and guide reforms that will transform India into a strong and vibrant knowledge economy. In this context, the focus areas for NPTEL project have been i) higher education, ii) professional education, iii) distance education and iv) continuous and open learning, roughly in that order of preference. </li><br>
							<li>Manpower requirement for trained engineers and technologists is far more than the number of qualified graduates that Indian technical institutions can provide currently. Among these, the number of institutions having fully qualified and trained teachers in all disciplines being taught forms a small fraction. A majority of teachers are young and inexperienced and are undergraduate degree holders. Therefore, it is important for institutions like IITs, IISc, NITs and other leading Universities in India to disseminate teaching/learning content of high quality through all available media. NPTEL would be among the foremost and an important step in this direction and will use technology for dissemination. </li><br>
							<li>India needs many more teachers for effective implementation of higher education in professional courses. Therefore, methods for training young and inexperienced teachers to enable them carry out their academic responsibilities effectively are a must. NPTEL contents can be used as core curriculum content for training purposes.</li><br>
							<li>A large number of students who are unable to attend scholarly institutions through NPTEL will have access to quality content from them.</li><br>
							All those who are gainfully employed in industries and all other walks of life and who require continuous training and updating their knowledge can benefit from well-developed and peer-reviewed course contents by the IITs and IISc.
						</ul>
					</div>	
					<div class="panel faqpanel">
						<div class="well fawell" data-toggle="collapse" data-target="#3" data-parent="#accordion">3. Is it along the lines of OCW (Open Courseware by Massachusetts Institute of Technology, USA)?<i class="glyphicon glyphicon-menu-down pull-right"></i></div>
						<ul id="3" class="panel-collapse collapse fadata">
							<p>The answer is yes and no. The goals are the same, but the processes are different. Open courseware project by the MIT makes available course materials by Professors of MIT freely to the rest of the world. OCW also encourages self-learners. It is a laudable exercise no doubt and has recorded millions of visits by students and teachers all over the world.<br>
							NPTEL also has proposed to provide open course materials for engineering and science students and teachers (freely). The differences between the two programmes are as follows:</p>
							<li>NPTEL is a curriculum building exercise and is directed towards providing learning materials in Science and Engineering by adhering to the syllabi of All India Council for Technical Education and the slightly modified curricula of major affiliating Universities. The NPTEL faculty have modularized their courses into core concepts which every one of these institutions may teach and topics which are add-ons to the course. The courses are well structured and are elaborate with details wherever the faculty members have felt the need. <b>Institutions are encouraged to build their own versions of NPTEL courses based on their curriculum design using the NPTEL materials and collective experience of all IITs and IISc in TEL.</b> They are meant to fill the large gap that exists between the current expertise level of faculty in institutions of higher learning such as the IITs/IISc and those in private and other government aided engineering institutions in India.  </li><br>
							<li>Secondly, the focus is to build at least one version of each course offered in all of Science and Engineering in India, from B.Tech. / B.Sc. to Ph. D. programs.</li><br>
							<li>Thirdly, NPTEL courses will be taken to the teachers through many workshops both at present and in the future. The interaction between teachers in various colleges and the course developers in IITs/IISc is a mandatory requirement for NPTEL.</li><br>
							<li>The fourth aspect is to build in the immediate future, a course-specific web space for each course where students, teachers and other users anywhere in India and outside would be encouraged to create threaded discussions. In this area, direct interaction among students all over the world and teachers would be encouraged through a bulletin-board approach/threaded discussions with the help of moderators who would be appointed for this purpose. The purpose is to eventually build a digital library for each subject and create Frequently Asked Questions (FAQ) for the section. </li><br>
							<li>The fifth aspect of difference between NPTEL and OCW is that there are 110 video (approximately 4500 hours) lecture courses from phase I and there will be about 400 video lecture courses (with about 16000 hours of lectures) at the end of phase II. In addition, IITs have large repositories of video lectures prepared already from their own efforts outside of NPTEL and these are also being made available as free and open educational resources for all. When this is completed, this will be the <b>largest video repository of technical lecture-courses in the world</b> in the streaming video format and will be helpful to everyone who is interested in enhancing his/her learning.</li><br>
							Despite these differences between MIT OCW and NPTEL, it is important to recognize that the goals of these two programmes are absolutely the same educate, get educated with technology and prosper. In India, the means have to be different given the complexity of the problem of education. 
						</ul>
					</div>
					<div class="panel faqpanel">
						<div class="well fawell" data-toggle="collapse" data-target="#4" data-parent="#accordion">4. How is NPTEL implemented?<i class="glyphicon glyphicon-menu-down pull-right"></i></div>
							<ul id="4" class="panel-collapse collapse fadata">
								There are two committees, the National Programme Committee (NPC) headed by the Joint Secretary, Higher Education, MHRD and the Programme Implementation Committee (PIC), headed by Professor M. S. Ananth, Director IIT Madras and Professor in Chemical Engineering.
								The NPC oversees implementation of the programme and offers policy guidelines and financial structure. Some of the NPC members are also members of the PIC. The PIC enables the smooth functioning of the project in several phases and takes care of content creation and technology implementation. Members of the PIC meet periodically (about once every three months) to study the progress and issues related to coursework development.
								In each IIT/IISc faculty are nominated as TEL coordinators to interact with their colleagues and encourage them to prepare course materials and offer technical and financial assistance using funds sanctioned for that purpose. In addition, two National coordinators, one for web based development and one for video lectures offer assistance and oversee the National programme. Groups are formed for solving specific technology or pedagogy related issues and arrive at general guidelines for faculty preparing course materials. In the first phase of the programme about 350 faculty members in all partner institutions worked together to deliver lecture contents. In the next phase this is likely to increase to well over 1000 faculty. Other Institutions such as NITs and major University faculty are also likely to participate.
							</ul>
					</div>	
					<div class="panel faqpanel">
						<div class="well fawell" data-toggle="collapse" data-target="#5" data-parent="#accordion">5. What is there for industries in NPTEL?<i class="glyphicon glyphicon-menu-down pull-right"></i></div>
							<ul id="5" class="panel-collapse collapse fadata">
								Pretty much everything, if they are looking for all-round, well-educated, conceptually sound graduates as opposed to people with specific skill set. Even in the latter case, NPTEL has something to offer for each skill set. Industry can adapt one or more courses on a specific subject to train student population and offer them better financial rewards and career opportunities.<br>
								Through NPTEL, a part of the IIT training, flavour and the rigour with which they are given is made available to teachers and student community at large. Therefore, IT and core engineering industries can join hands to do the following:<br><br>
								<li>Design comprehensive tests and assignments as well as student assessment online in related, engineering- based course contents in most branches so that the student skill sets are improved.</li><br>
								<li>Sponsor local teams with the NPTEL faculty for training teachers and students to think on analytic methods of study as opposed to mere rote learning which the current University examination system seems to promote.</li><br>
								<li>Design parameters for effective learning of any given subject and offer expert training to colleges to ensure that students do this in addition to their University examinations. Reward mechanism for students can be created.</li><br>
								<li>Contribute to the process of creation of contents in several new courses by faculty in IIT/IISc and create courses which are not part of the AICTE curricula but which would address the short and long term training requirements of industries. This would enable better interaction and understanding of mutual requirements of academia and industry.</li><br>
								The value addition can be enormous if right partnerships are formed between industry and academia.
							</ul>
