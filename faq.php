							<p>India is a vast country whose engineering student population outnumbers every other country, except possibly China. The objectives in Phase II are to create contents for science and engineering courses in all major disciplines as well as specialized and newly developing interdisciplinary subjects for which there is very little academic expertise in private colleges. In addition, helping colleges through workshops and discussion boards for implementing NPTEL content in their curriculum will be undertaken as a primary and most important activity. This is the most significant difference between open educational resources developed worldwide and NPTEL. IIT/IISc faculty would be encouraged to incorporate feedback from user community in their courses and update them.</p>
