						<div class="well fawell" data-toggle="collapse" data-target="#8" data-parent="#accordion">8. What are the mechanisms for promoting NPTEL?<i class="glyphicon glyphicon-menu-down pull-right"></i></div>
						<ul id="8" class="panel-collapse collapse fadata">
							Several mechanisms have been proposed.<br><br>
							<li>Conduct course specific workshops by bringing the faculty who developed the course with teachers who are likely to use the lecture material. In the future, a few representative students from many user institutions may be invited to participate in these workshops and give their feedback.</li><br>
							<li>Conduct workshops in selected regions all over the country so that a large body of students can also participate and learn the process of usage.</li><br>
							<li>Create subject index and keyword search for both video and web materials so that students can search for relevant materials across courses through a search engine. </li><br>
							<li>Create course-specific bulletins/discussion boards in the web site so that students can ask questions about the course material. Open learning will be supported by permitting answers by interested students and teachers and with occasional moderation of discussions by course developers.</li><br>
							<li>Create a course-specific Edupedia (similar to the powerful concept of Wikipedia) with the help of qualified teachers across the country and a digital library relevant to course materials and make them available in the course area. </li><br>
							<li>Create course specific FAQ through all of the above.</li><br>
							<li>Encourage teachers in various colleges to adapt the materials to prepare localized versions suitable for the examination system of that college.</li><br>
							<li>Share the expertise on e-learning, content development, content dissemination with interested Institutions so that they can set up their own e-learning portals.</li><br>
							<li>Distribute the NPTEL content-both web and video to any interested institution for its internal use.</li><br>
							<li>Set up a video-on-demand facility in IITs and IISc with sufficient exclusive internet bandwidth for making video lectures available in the streaming format. This will help not only the students and teachers but also industry professionals and open learners.</li><br>
						</ul>
